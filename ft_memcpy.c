#include"libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	d = dst;
	s = (unsigned char *)(src);
	i = 0;
	if (d == '\0' && s == '\0')
		return (NULL);
	if (d < s)
	{
		while (n)
		{
			n--;
			d[n] = s[n];
		}
	}
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (d);
}
